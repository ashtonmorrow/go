// Dry-run preview of voice fixes to go_cities Mike-voice fields.
// Run: node scripts/dry-run-go-cities-voice.mjs
// Output: scripts/go_cities-voice-dry-run.md
//
// Auto-fix patterns:
//   - "genuinely" / "honestly" / "frankly" (throat-clearing hedges) -> deleted
//   - em dash -> ", "
//   - "delivers" -> "is as good as advertised"
//
// Manual rewrites needed for "the headline X" and "earns the X" patterns,
// which require contextual judgment and are NOT touched by this script.

import fs from 'node:fs';
import { createClient } from '@supabase/supabase-js';

const env = fs.readFileSync('.env.local', 'utf8')
  .split('\n').filter(l => l && !l.startsWith('#'));
for (const l of env) {
  const [k, ...v] = l.split('=');
  if (k) process.env[k.trim()] = v.join('=').trim().replace(/^["']|["']$/g, '');
}

const sb = createClient(
  process.env.NEXT_PUBLIC_SUPABASE_URL,
  process.env.STRAY_SUPABASE_SERVICE_ROLE_KEY
);

const patterns = [
  { name: 'genuinely', re: /\bgenuinely\b\s*/gi, replacement: '' },
  { name: 'honestly',  re: /\bhonestly\b\s*/gi,  replacement: '' },
  { name: 'frankly',   re: /\bfrankly\b\s*/gi,   replacement: '' },
  { name: 'em-dash',   re: /—/g,                 replacement: ', ' },
  { name: 'delivers',  re: /\bdelivers\b/g,      replacement: 'is as good as advertised' },
];

const { data: cit, error } = await sb.from('go_cities')
  .select('id,slug,name,about,why_visit,avoid');

if (error) { console.error(error); process.exit(1); }

const lines = [
  '# go_cities voice-fix dry-run',
  '',
  'Generated by autonomous pass. Review before applying.',
  '',
  'Auto-fix patterns:',
  '- `genuinely` / `honestly` / `frankly` (throat-clearing hedges) -> deleted',
  '- em dash -> `, `',
  '- `delivers` -> `is as good as advertised`',
  '',
  'Manual rewrites needed for "the headline X" and "earns the X" patterns,',
  'which require contextual judgment. NOT touched by this script.',
  '',
];

let n = 0;

for (const c of cit) {
  const before = { about: c.about, why_visit: c.why_visit, avoid: c.avoid };
  const after  = { about: c.about, why_visit: c.why_visit, avoid: c.avoid };
  let changed = false;

  for (const fld of ['about', 'why_visit', 'avoid']) {
    if (!after[fld]) continue;
    let v = after[fld];
    for (const p of patterns) {
      const nv = v.replace(p.re, p.replacement);
      if (nv !== v) { v = nv; changed = true; }
    }
    v = v.replace(/\s+/g, ' ').replace(/\s+([.,;:])/g, '$1').trim();
    after[fld] = v;
  }

  if (changed) {
    n++;
    lines.push(`## ${c.slug} (${c.name})`);
    for (const fld of ['about', 'why_visit', 'avoid']) {
      if (before[fld] !== after[fld]) {
        lines.push(`### ${fld}`);
        lines.push(`BEFORE: ${(before[fld] || '').slice(0, 500)}`);
        lines.push(`AFTER:  ${(after[fld]  || '').slice(0, 500)}`);
        lines.push('');
      }
    }
  }
}

lines.push('---', '', `Total: ${n} cities would be updated.`);

fs.writeFileSync('scripts/go_cities-voice-dry-run.md', lines.join('\n'));
console.log(`Wrote scripts/go_cities-voice-dry-run.md with ${n} affected cities.`);
